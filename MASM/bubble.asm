ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUMLIST DW 01H,24H,20H,16H
COUNT EQU 4
DATA ENDS
CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	MOV DX,COUNT-1
AGAIN0:	MOV CX,DX
	MOV SI,OFFSET NUMLIST	
AGAIN1:	MOV AX,[SI]
	CMP AX,[SI+2]
	JL PR1
	XCHG [SI+2],AX
	XCHG [SI],AX
PR1:	ADD SI,02
	LOOP AGAIN1
	DEC DX
	JNZ AGAIN0
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
