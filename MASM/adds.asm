ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUMLIST DB 01H,24H,20H,16H
COUNT EQU 4
RESULT DW 01 DUP(?)
DATA ENDS
CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	MOV CX,COUNT
	XOR AX,AX
	XOR BX,BX
	MOV SI,OFFSET NUMLIST
AGAIN:	MOV BX,[SI]
	ADD AX,BX
	INC SI
	DEC CX
	JNZ AGAIN
	MOV DI,OFFSET RESULT
	MOV [DI],AX
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
