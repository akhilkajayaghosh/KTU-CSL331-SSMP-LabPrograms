ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUMBERS DW 10 DUP(?)
COUNT DW 10
DATA ENDS
CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	MOV SI,OFFSET NUMBERS
	MOV BX,0
	MOV AX,1
	MOV [SI],BX
	ADD SI,2
	MOV CX,COUNT
	SUB CX,2
AGAIN:	ADD AX,BX
	MOV [SI],AX
	MOV AX,BX
	MOV BX,[SI]
	ADD SI,2
	DEC CX
	JNZ AGAIN
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
