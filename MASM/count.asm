ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUMLIST DB 01H,25H,48H,36H,42H,22H
COUNT EQU 6
DATA ENDS
CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	MOV CX,COUNT
	XOR BX,BX
	XOR DX,DX
	MOV SI,OFFSET NUMLIST
AGAIN:	MOV AX,[SI]
	ROR AX,01
	JC ODD
	INC DX
	JMP EVENT
ODD:	INC BX
EVENT:	INC SI
	DEC CX
	JNZ AGAIN
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
